export const generate = (howMany: number, sortByAtomicNumber: boolean, unique: boolean) => {
    const max = periodcTable.length - 1;
    const randomUniqueAtoms: Set<IAtom> = new Set;
    const randomAtoms: Array<IAtom> = [];
    for (let i = 0; i < howMany; i++) {
        const atom = periodcTable[Math.round(Math.random() * max)];
        unique ? randomUniqueAtoms.add(atom) : randomAtoms.push(atom);
    }
    const unsorted = unique ? [...randomUniqueAtoms] : randomAtoms;
    if (sortByAtomicNumber) {
        return unsorted.sort((a: IAtom, b: IAtom) => a.atomicNumber - b.atomicNumber);
    }
    return unsorted;
};